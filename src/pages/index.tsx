import { SignInButton, SignOutButton, useUser } from "@clerk/nextjs";
import { type NextPage } from "next";
import Head from "next/head";

const Home: NextPage = () => {

  const {user, isSignedIn, isLoaded} = useUser();

  if(!isLoaded) return <div className="text-white text-4xl text-bold">Loading...</div>;

  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-col items-center justify-center bg-gradient-to-b from-[#2e026d] to-[#15162c]">
        { isSignedIn ? (
          <div className="text-white">
            <h1 className="text-4xl font-bold">Welcome, {user.fullName}!</h1>
            <p className="text-xl">You are signed in.</p>
            <SignOutButton>
              <button className="mt-2 bg-slate-400 px-6 py-2 rounded">Sign out</button>
            </SignOutButton>
          </div>
        ) : (
          <div className="text-white">
            <h1 className="text-4xl font-bold">Welcome!</h1>
            <p className="text-xl">You are not signed in.</p>
            <SignInButton >
              <button className="mt-2 bg-slate-400 px-6 py-2 rounded">Sign in</button>
            </SignInButton>
          </div>
        )}
        
      </main>
    </>
  );
};

export default Home;
